        What is VanCity Social?
        <div id="aboutClose"><img src='images/xw.png'></div>
    </div>
    <p>
        It's an invite-only social network where Vancouverites can connect with other people within the city.
    </p>
    
    <div class='sectionHeader'>Why invite-only?</div>
    <p>Keeping the site invite-only is an easy way to ensure the user base stays relevent. Each user is allowed to give out three invitations to friends.</p>
    
    <div class="sectionHeader">How is VanCity Social different than other social networks?</div>
    <p>Unlike Facebook or Twitter, VanCity Social isn't focused on friends or followers. Instead, all users share the same timeline and their posts are organized into topics. By favoriting a topic, and posting,browsing or commenting in that topic regularly, users can connect with other likeminded people in the city.</p>
    <p>For example, let's say you're really into hockey. You might want to favorite the topic 'Canucks', so it's in your list of easily accessible topics in the Topics Menu on the left. Whenever you want to discuss the latest victory, click the 'Canucks' topic in the menu and the main timeline will show only Canucks-related posts. Now you can interact with other hockey fans in the city, reply to other people posts, send users direct messages, or add a user as a friend.</p>
    
    <div class="sectionHeader">What's the News Feed?</div>
    <p>Another thing that sets VanCity Social apart is it's new aggregator. The News Feed ensures user stay up to date with the latest happenings in the city, with updates from all of Vancouver's most trusted online sources, like The Georgia Straigh, Metro News Vancouver, VanCity Buzz, Miss 604, Vancouver Is Awesome, and many others.</p>
